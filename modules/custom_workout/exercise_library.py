# ── Helper Functions ──────────────────────────────────────────────────────────

def get_all_muscles() -> dict:
    """Get all muscle groups organized by body part."""
    return MUSCLE_GROUPS


def get_exercises_for_muscle(muscle: str) -> list:
    """Get all exercises for a specific muscle."""
    return EXERCISE_LIBRARY.get(muscle.lower(), [])


def get_exercises_for_goal(goal: str, muscle: str) -> list:
    """
    Get exercises for muscle sorted by goal.
    Fat loss  → compound + cardio first
    Bulk      → heavy compound first
    Strength  → compound first
    All shown regardless of goal
    """
    exercises = EXERCISE_LIBRARY.get(muscle.lower(), [])

    if not exercises:
        return []

    if goal == "fat_loss":
        return sorted(
            exercises,
            key=lambda x: (
                0 if x["type"] == "compound" else
                1 if x["type"] == "cardio"   else 2
            )
        )
    elif goal in ["bulk", "strength", "bodybuilding"]:
        return sorted(
            exercises,
            key=lambda x: 0 if x["type"] == "compound" else 1
        )

    return exercises


def get_total_exercises() -> int:
    """Get total number of exercises in library."""
    return sum(len(v) for v in EXERCISE_LIBRARY.values())